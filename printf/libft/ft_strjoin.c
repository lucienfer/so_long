#include "libft.h"

char	*ft_strjoin(char const *s1, char const *s2)
{
	char	*str;
	int		i;
	int		u;

	u = 0;
	i = 0;
	if (!s1 && !s2)
		return (0);
	str = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2)) + 1);
	if (!str)
		return (0);
	while (s1[i])
	{
		str[i] = s1[i];
		i++;
	}
	while (s2[u])
	{
		str[i] = s2[u];
		i++;
		u++;
	}
	str[i] = 0;
	return (str);
}
